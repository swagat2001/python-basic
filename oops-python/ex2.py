class MATH_FUN():
    def __init__(self,a,b):
        self.a = a
        self.b = b
        
    def ADD(self):
        return(self.a+self.b)
    
    def MUL(self):
        return(self.a*self.b)
    
    def DIV(self):
        return(self.a/self.b)
    
    def SUB(self):
        return(self.a-self.b)